#Number Type Variables
int_var = 10
float_var = 20.5
complex_var = 3 + 4j

print("Integer Variable:", int_var)
print("Float Variable:", float_var)
print("Complex Variable:", complex_var)

#String Type Variables
str_var = "Hello, World!"
char_var = 'A'

print("String Variable:", str_var)
print("Character Variable:", char_var)

#List Type Variable
list_var = ["Alu", "Mula", 10, 20.5]

print("List Variable:", list_var)


